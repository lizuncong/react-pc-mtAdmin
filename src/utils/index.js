// 生成文件URL，仅用于本地上传图片时，选择完图片在本地显示
export const getFileUrl = (file) => {
  let url;
  const agent = navigator.userAgent;
  if (agent.indexOf('MSIE') >= 1) {
    url = file.value;
  } else if (agent.indexOf('Firefox') > 0 || agent.indexOf('Chrome') > 0) {
    url = window.URL.createObjectURL(file);
  }
  return url;
};

// 解析URL的参数
export const getQueryObject = (url) => {
  url = url == null ? window.location.href : url;
  const search = url.substring(url.lastIndexOf('?') + 1);
  const obj = {};
  const reg = /([^?&=]+)=([^?&=]*)/g;
  search.replace(reg, (rs, $1, $2) => {
    const name = decodeURIComponent($1);
    let val = decodeURIComponent($2);
    val = String(val);
    obj[name] = val;
    return rs;
  });
  return obj;
};


// 复数
function pluralize(time, label) {
  if (time === 1) {
    return time + label;
  }
  return `${time + label}s`;
}

// time 时间戳，单位秒
export function timeAgo(time) {
  const between = Date.now() / 1000 - Number(time);
  if (between < 3600) {
    return pluralize(~~(between / 60), ' minute');
  }
  if (between < 86400) {
    return pluralize(~~(between / 3600), ' hour');
  }
  return pluralize(~~(between / 86400), ' day');
}

// 整数格式化，如 10000 => 10k。 num整数，digits小数位
export function numberFormatter(num, digits) {
  const si = [
    { value: 1E18, symbol: 'E' },
    { value: 1E15, symbol: 'P' },
    { value: 1E12, symbol: 'T' },
    { value: 1E9, symbol: 'G' },
    { value: 1E6, symbol: 'M' },
    { value: 1E3, symbol: 'k' },
  ];
  for (let i = 0; i < si.length; i += 1) {
    if (num >= si[i].value) {
      const temp = (num / si[i].value).toFixed(digits);
      return temp.replace(/\.0+$|(\.[0-9]*[1-9])0+$/, '$1') + si[i].symbol;
    }
  }
  return num.toString();
}

// 整数分割，如10000 => 10,000
export function toThousandFilter(num) {
  return (+num || 0).toString().replace(/^-?\d+/g, (m) => m.replace(/(?=(?!\b)(\d{3})+$)/g, ','));
}


